// Array flat de todos os icones (para getRandomSpellIcon)
const _ALL_SPELL_ICONS = SPELL_ICON_LIBRARY.reduce((acc, cat) => acc.concat(cat.icons), []);

function getRandomSpellIcon() {
    return _ALL_SPELL_ICONS[Math.floor(Math.random() * _ALL_SPELL_ICONS.length)];
}

function iconBadge(type, value) {
    const colors = { pa: '#3b82f6', pm: '#22c55e', pe: '#eab308' };
    return '<span class="stat-badge" style="--badge-color:' + (colors[type] || '#888') + '" title="' + type.toUpperCase() + '">' + value + '</span>';
}

function iconElem(element) { return GAME_ICONS[element] || GAME_ICONS.none; }

function formatSpellCosts(spell) {
    let parts = [];
    if (spell.paCost) parts.push(iconBadge('pa', spell.paCost));
    if (spell.pmCost) parts.push(iconBadge('pm', spell.pmCost));
    if (spell.peCost) parts.push(iconBadge('pe', spell.peCost));
    return parts.join(' ');
}
