// Usados quando o slot está vazio - renderizados como outline (stroke, sem fill)
const EQUIP_SLOT_DEFAULTS = {
    helmet:     'pointy-hat',
    chest:      'breastplate',
    boots:      'boots',
    cape:       'cape',
    belt:       'belt',
    ring:       'ring',
    amulet:     'pendant',
    weapon:     'broadsword',
    weaponLeft: 'round-shield',
    bag:        'empty-bag',
    tool:       'generic-tool'
};

// ========== FUNÇÕES DE RENDERIZAÇÃO ==========

// Busca um ícone por ID em qualquer slot
function getEquipIconById(iconId) {
    const slots = Object.keys(EQUIP_ICON_LIBRARY);
    for (let i = 0; i < slots.length; i++) {
        const icons = EQUIP_ICON_LIBRARY[slots[i]];
        for (let j = 0; j < icons.length; j++) {
            if (icons[j].id === iconId) return icons[j];
        }
    }
    return null;
}

// Busca paleta por ID
function getEquipPaletteById(paletteId) {
    for (let i = 0; i < EQUIP_COLOR_PALETTES.length; i++) {
        if (EQUIP_COLOR_PALETTES[i].id === paletteId) return EQUIP_COLOR_PALETTES[i];
    }
    return EQUIP_COLOR_PALETTES[0]; // fallback: iron
}

// Mapeia slot de equipamento para categoria de ícone
function getIconCategoryForSlot(slot) {
    if (!slot) return null;
    if (slot === 'weapon' || slot === 'weaponLeft') return 'weapon';
    if (slot.startsWith('ring')) return 'ring';
    if (slot === 'amulet') return 'amulet';
    if (slot === 'tool' || (slot && slot.startsWith('tool_'))) return 'tool';
    return slot; // helmet, chest, boots, cape, belt
}

// Cria elemento SVG de um ícone multi-zona com cores
// iconData: objeto do EQUIP_ICON_LIBRARY (com .zones)
// colors: { body, detail, accent, gem } OU paletteId string
// size: tamanho em px
// outline: se true, renderiza apenas contorno (para slots vazios)
function renderEquipIconSvg(iconData, colors, size, outline) {
    if (!iconData || !iconData.zones) return null;
    size = size || 36;

    if (typeof colors === 'string') {
        colors = getEquipPaletteById(colors);
    }
    if (!colors) colors = EQUIP_COLOR_PALETTES[0];

    var svg = document.createElementNS('http://www.w3.org/2000/svg', 'svg');
    svg.setAttribute('viewBox', '0 0 512 512');
    svg.setAttribute('width', size);
    svg.setAttribute('height', size);
    svg.style.display = 'block';

    var typeColorMap = {
        body: colors.body || '#888',
        detail: colors.detail || '#666',
        accent: colors.accent || '#aaa',
        gem: colors.gem || '#ddd'
    };

    for (var i = 0; i < iconData.zones.length; i++) {
        var zone = iconData.zones[i];
        var path = document.createElementNS('http://www.w3.org/2000/svg', 'path');
        path.setAttribute('d', zone.d);

        if (outline) {
            path.setAttribute('fill', 'none');
            path.setAttribute('stroke', typeColorMap[zone.type] || '#555');
            path.setAttribute('stroke-width', '8');
            path.setAttribute('stroke-linejoin', 'round');
        } else {
            path.setAttribute('fill', typeColorMap[zone.type] || '#888');
        }

        svg.appendChild(path);
    }

    return svg;
}

// Renderiza ícone padrão do slot (contorno cinza escuro)
function renderEmptySlotIcon(slotType, size) {
    var normalizedSlot = slotType;
    if (slotType && slotType.startsWith('ring')) normalizedSlot = 'ring';
    if (slotType && slotType.startsWith('bag')) normalizedSlot = 'bag';
    if (slotType && slotType.startsWith('tool_')) normalizedSlot = 'tool';

    var iconId = EQUIP_SLOT_DEFAULTS[normalizedSlot];
    if (!iconId) return null;

    var iconData = getEquipIconById(iconId);
    if (!iconData) return null;

    var outlineColors = { body:'#778899', detail:'#667788', accent:'#8899aa', gem:'#99aabb' };
    return renderEquipIconSvg(iconData, outlineColors, size || 32, true);
}

// Renderiza ícone de um item de equipamento
// item: objeto do item (precisa ter .svgIcon = { shape, palette } ou .svgIcon = { shape, colors })
function renderItemEquipIcon(item, size) {
    if (!item || !item.svgIcon) return null;

    var iconData = getEquipIconById(item.svgIcon.shape);
    if (!iconData) return null;

    var colors;
    if (item.svgIcon.palette) {
        colors = getEquipPaletteById(item.svgIcon.palette);
    } else if (item.svgIcon.colors) {
        colors = item.svgIcon.colors;
    } else {
        colors = EQUIP_COLOR_PALETTES[0];
    }

    return renderEquipIconSvg(iconData, colors, size || 36, false);
}

// Retorna lista de ícones disponíveis para um slot específico
function getIconsForSlot(slot) {
    var category = getIconCategoryForSlot(slot);
    if (!category) return [];
    return EQUIP_ICON_LIBRARY[category] || [];
}

